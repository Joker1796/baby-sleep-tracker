// Ободряющий разбор «было → стало» по этапам. Показывается в утренней сводке,
// когда возраст малыша соответствует этапу.
export const STAGE_PROGRESS: { fromM: number; toM: number; text: string }[] = [
  {
    fromM: 2,
    toM: 3,
    text: 'В начале был хаос: укладывание по настроению, сны по 20–30 минут только на руках, ночные пробуждения каждый час-два и паника при каждом писке. Теперь у вас есть чёткое окно бодрствования 75–90 минут, сны по 40–60 минут в кроватке, ночью малыш просыпается лишь 1–2 раза, вы научились выдерживать паузы и действовать по алгоритмам. У вас появились система и план — вы большие молодцы.'
  }
]

export function stageProgressFor(ageMonths: number): string | null {
  const s = STAGE_PROGRESS.find(x => ageMonths >= x.fromM && ageMonths < x.toM)
  return s ? s.text : null
}
